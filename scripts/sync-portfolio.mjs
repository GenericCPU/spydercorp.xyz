#!/usr/bin/env node
/**
 * Scans public/work/<project>/*.{png,jpg,jpeg,webp} and writes src/data/portfolio.ts
 * Drop screenshots into public/work/<project-slug>/ then run: node scripts/sync-portfolio.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const workDir = path.join(root, 'public', 'work');
const outFile = path.join(root, 'src', 'data', 'portfolio.ts');

const IMAGE_RE = /\.(png|jpe?g|webp)$/i;

const projectMeta = {
  bringmebuds: {
    title: 'BringMeBuds.ca',
    category: 'Cannabis delivery · Halifax',
    url: 'https://bringmebuds.vercel.app',
    description:
      'Full shop rebuild — browse, cart, coupons, delivery logic. Mobile-first UX designed to reduce friction and complete more orders.',
    tags: ['E-commerce UX', 'Brand', 'Engineering'],
  },
};

function titleFromFilename(file) {
  return file
    .replace(/^\d+-/, '')
    .replace(/\.(png|jpe?g|webp)$/i, '')
    .replace(/-/g, ' ')
    .replace(/\b\w/g, (c) => c.toUpperCase());
}

function scan() {
  if (!fs.existsSync(workDir)) {
    fs.mkdirSync(workDir, { recursive: true });
    return [];
  }

  const projects = fs
    .readdirSync(workDir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort();

  return projects.map((slug) => {
    const dir = path.join(workDir, slug);
    const files = fs
      .readdirSync(dir)
      .filter((f) => IMAGE_RE.test(f))
      .sort();

    const meta = projectMeta[slug] ?? {
      title: slug.replace(/-/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase()),
      category: 'Client project',
      url: '',
      description: '',
      tags: [],
    };

    return {
      id: slug,
      ...meta,
      shots: files.map((file, i) => ({
        src: `/work/${slug}/${file}`,
        alt: `${meta.title} — ${titleFromFilename(file)}`,
        caption: titleFromFilename(file),
        featured: i === 0,
      })),
    };
  });
}

const projects = scan();

const ts = `/* Auto-generated by scripts/sync-portfolio.mjs — do not edit by hand */
export const portfolio = ${JSON.stringify(projects, null, 2)} as const;

export type PortfolioProject = (typeof portfolio)[number];
export type PortfolioShot = PortfolioProject['shots'][number];
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, ts);
console.log(`Wrote ${projects.length} project(s), ${projects.reduce((n, p) => n + p.shots.length, 0)} shot(s) → ${path.relative(root, outFile)}`);
